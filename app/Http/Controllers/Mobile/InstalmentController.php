    public function pay(Request $request, $id)
    {
        $instalment = Instalment::findOrFail($id);
        $instalment->is_paid = 1;
        $instalment->date_paid = date('Y-m-d H:i:s');
        if($instalment->save()) {
            return redirect()->back();
        }
    }

